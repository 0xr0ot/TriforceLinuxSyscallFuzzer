
#include <sys/ioctl.h>

int main(int argc, char **argv)
{
    unsigned char buf[32*4] = {
      0x0a, 0x00, 0x00, 0x00,  // type
      0x00, 0x00, 0x10, 0x00,  // line
      0xff, 0xe7, 0x00, 0x00,  // port
      0x00, 0x00, 0x00, 0x00,  // irq
      0x00, 0x10, 0x00, 0x10,  // flags
      0xA5, 0xC9, 0x0E, 0x00,  // xmit fifo size
      0x00, 0x02, 0xF8, 0xFF,  // custom divisor
      0xFF, 0xD7, 0x00, 0x00,  // baud base
    };
    ioctl(0, TIOCSSERIAL, buf);
    return 0;
}

